#include QMK_KEYBOARD_H


#define _QWERTY 0
#define _LOWER 1
#define _RAISE 2
#define _ADJUST 3

#define KC_ESCC MT(MOD_LCTL, KC_ESC)

uint8_t prev = _QWERTY;
uint32_t desired = 1;
uint16_t hue = 85;
uint16_t sat = 255;
uint16_t val = 10;

enum custm_keycodes {
  QWERTY = SAFE_RANGE,
  LOWER,
  RAISE,
  ADJUST,
};

void get_hsv(void) {
    hue = rgblight_get_hue();
    sat = rgblight_get_sat();
    val = rgblight_get_val();
}

void reset_hsv(void) {
    rgblight_sethsv(hue, sat, val);
}

void matrix_init_user() {
    rgblight_mode(desired);
    rgblight_enable();
    reset_hsv();
}

uint32_t layer_state_set_user(uint32_t state) {
  uint8_t layer = biton32(state);
      switch (layer) {
        case _QWERTY:
          rgblight_mode(1);
          reset_hsv();
          break;

        case _LOWER:
          rgblight_mode(5);
          rgblight_sethsv(28, 255, 100);
          break;

        case _RAISE:
          rgblight_mode(23);
          rgblight_sethsv(213, 255, 100);
          break;

        case _ADJUST:
          rgblight_mode(5);
          rgblight_sethsv(128, 255, 100);
          break;
      }
  prev = layer;
  return state;
}

